# ----------------------- PEP 8 SUMMARY -----------------------

# 1. Indentation
# - Use 4 spaces per indentation level (no tabs)

# 2. Line Length
# - Limit all lines to 79 characters
# - Limit comments/docstrings to 72 characters

# 3. Blank Lines
# - Use 2 blank lines between top-level functions/classes
# - Use 1 blank line between methods in a class

# 4. Imports
# - Put all imports at the top of the file
# - Import order: standard libs â†’ third-party libs â†’ local code
# - Use one import per line

# 5. Whitespace Usage
# - Add spaces around operators (+, -, *, /, =, etc.)
# - Add space after commas
# - No extra spaces inside (), [], {}, or before :

# 6. Naming Conventions
# - Variables and functions: snake_case
# - Constants: UPPER_CASE
# - Classes: PascalCase (CamelCase)
# - Private members: _start_with_underscore

# 7. Functions and Classes
# - Name them clearly and consistently
# - Always add a docstring inside them

# 8. Comments
# - Write full-sentence, meaningful comments
# - Use inline comments only when necessary
# - Start comments with a capital letter and leave a space after #

# 9. Docstrings
# - Use triple double-quotes """ """
# - Place directly under function/class definitions
# - Describe what the code does, what parameters it takes, and what it returns

# 10. Comparisons
# - Use 'is' or 'is not' when comparing with None
# - Avoid '== True' or '== False'; use the value directly

# 11. Boolean Usage
# - Use 'if x:' instead of 'if x == True'

# 12. Trailing Spaces
# - No extra spaces at the end of any line

# 13. Parentheses
# - Donâ€™t use extra parentheses if not required

# 14. Quotes
# - Use single (' ') or double (" ") quotes consistently throughout your code

# 15. Encoding
# - Use UTF-8 encoding if needed at the top of your file:
#   # -*- coding: utf-8 -*-

