# // Think of a module like:
# A toolbox  that contains tools (functions, variables, classes) you can use in your program.

#  Why use modules?
# Reusability: You write code once and use it in many programs.
#
# Organization: Break large code into smaller, manageable files.
#
# Built-in power: You get access to thousands of built-in modules.
#
#             Type	                                                   Example
        # Built-in module	                                       math, random, os
        # User-defined	                                           Your own .py files
        # Third-party	                                           numpy, pandas, flask (installed via pip)


